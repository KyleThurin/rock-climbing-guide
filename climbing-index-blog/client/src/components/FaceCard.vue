<section id="facts-section">
    <h3>Climbing Area (also called a Crag)</h3>
    <blockquote>A larger geographical location where climbers gather to practice and enjoy the sport</blockquote>
    <blockquote>Areas often have many different routes and climbing opportunities, making them diverse and challenging</blockquote>
    <blockquote>Examples include specific cliffs, formations, or even indoor climbing walls</blockquote>
    <h3>Face</h3>
    <blockquote>A face is the actual rock wall that climbers ascend</blockquote>
    <blockquote>It can be vertical, overhanging, or even a slab depending on the terrain</blockquote>
    <blockquote>The features on a face, like cracks, holds, and other irregularities, are what climbers use to make their ascent</blockquote>
    <h3>Route</h3>
    <blockquote>A route is a specific path or line that a climber follows on a face</blockquote>
    <blockquote>It can be short and steep or long and multi-pitch, depending on the area and style of climbing</blockquote>
    <blockquote>Routes are often rated based on their difficulty, providing climbers with a sense of challenge and progression</blockquote>
    <blockquote>Routes can be sport climbs (with bolted protection), trad climbs (with the climber placing their own protection), or a mix of both</blockquote>
    <blockquote>Bouldering involves short, powerful, and often technical climbs on boulders, usually without ropes. </blockquote>
